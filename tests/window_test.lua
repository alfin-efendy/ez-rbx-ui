    local t2 = w:AddTab({ Name = "B" })
    h.expect(t1:IsSelected()).toBe(true)
    h.expect(t2:IsSelected()).toBe(false)
    t2.Button.MouseButton1Click:Fire()
    h.expect(t1:IsSelected()).toBe(false)
