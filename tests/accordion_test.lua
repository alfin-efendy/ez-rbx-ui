local h = require("tests.helper")
local R = h.loadLib()
local Accordion, Create = R.Accordion, R.Create

local function parentWithLayout()
  local p = Create("Frame", {})
  Create("UIListLayout", { Parent = p })
  return p
end

h.describe("accordion", function()
  h.it("container clips and is auto-size None (tween-controlled)", function()
    local p = parentWithLayout()
    local acc = Accordion.new({ Parent = p, Title = "Advanced", Icon = "settings-2" })
    h.expect(acc.Container.ClipsDescendants).toBe(true)
    h.expect(acc.Container.AutomaticSize.Name).toBe("None")
    h.expect(acc.Container.Parent).toBe(p)
  end)
  h.it("content uses UIListLayout + AutomaticSize.Y", function()
    local p = parentWithLayout()
    local acc = Accordion.new({ Parent = p, Title = "X" })
    h.expect(acc.Content.AutomaticSize.Name).toBe("Y")
    h.expect(acc.Content:FindFirstChildOfClass("UIListLayout") ~= nil).toBeTruthy()
  end)
  h.it("renders as a bordered card with a divider that shows only when expanded", function()
    local acc = Accordion.new({ Parent = parentWithLayout(), Title = "Adv", Expanded = false })
    h.expect(acc.Container:FindFirstChildOfClass("UIStroke") ~= nil).toBeTruthy()
    local divider = acc.Container:FindFirstChild("Divider")
    h.expect(divider ~= nil).toBeTruthy()
    h.expect(divider.Visible).toBe(false)
    acc:Expand()
    h.expect(divider.Visible).toBe(true)
  end)
  h.it("does not recolor the container on header hover", function()
    local acc = Accordion.new({ Parent = parentWithLayout(), Title = "X" })
    local base = acc.Container.BackgroundColor3
    acc.Header.MouseEnter:Fire()
    h.expect(acc.Container.BackgroundColor3).toBe(base)
  end)
  h.it("MountRow assigns incrementing LayoutOrder under Content", function()
    local p = parentWithLayout()
    local acc = Accordion.new({ Parent = p, Title = "X" })
    local a = Create("Frame", {}); local b = Create("Frame", {})
    local o1 = acc.MountRow(a); local o2 = acc.MountRow(b)
    h.expect(a.Parent).toBe(acc.Content)
    h.expect(o2 > o1).toBeTruthy()
  end)
  -- Motion (Task 2): caret rotation + spring height + slide reveal
  h.it("collapsed by default: header height, caret unrotated, content hidden", function()
    local a = Accordion.new({ Parent = Create("Frame", {}), Title = "Sec" })
    h.expect(a.Container.Size.Y.Offset).toBe(34)
    h.expect(a.Header:FindFirstChild("Caret").Rotation).toBe(0)
    h.expect(a.Content.Visible).toBe(false)
  end)
  h.it("Expand rotates caret to 90, shows content, hands height to AutomaticSize.Y", function()
    local a = Accordion.new({ Parent = Create("Frame", {}), Title = "Sec" })
    a:Expand()
    h.expect(a:IsExpanded()).toBe(true)
    h.expect(a.Content.Visible).toBe(true)
    h.expect(a.Header:FindFirstChild("Caret").Rotation).toBe(90)
    h.expect(a.Container.AutomaticSize.Name).toBe("Y")   -- engine fits content (no script re-measure)
  end)
  h.it("Collapse rotates caret back to 0, hides content, returns to header height", function()
    local a = Accordion.new({ Parent = Create("Frame", {}), Title = "Sec", Expanded = true })
    a:Collapse()
    h.expect(a:IsExpanded()).toBe(false)
    h.expect(a.Content.Visible).toBe(false)
    h.expect(a.Header:FindFirstChild("Caret").Rotation).toBe(0)
    h.expect(a.Container.Size.Y.Offset).toBe(34)
  end)
  h.it("Toggle flips expansion and returns the new state", function()
    local a = Accordion.new({ Parent = Create("Frame", {}), Title = "Sec" })
    h.expect(a:Toggle()).toBe(true)
    h.expect(a:Toggle()).toBe(false)
  end)
  -- Height is engine-driven (AutomaticSize.Y) when expanded, so tall/dynamic content (reactive
  -- paragraphs) keeps fitting WITHOUT any post-construction script write -- which is what fails on
  -- executors that deny the GUI capability to Heartbeat/signal handlers (content was clipped there).
  h.it("expanded => AutomaticSize.Y (engine-driven), collapsed => fixed header height", function()
    local acc = Accordion.new({ Parent = parentWithLayout(), Title = "X", Expanded = true })
    h.expect(acc.Container.AutomaticSize.Name).toBe("Y")
    acc:Collapse()
    h.expect(acc.Container.AutomaticSize.Name).toBe("None")
    h.expect(acc.Container.Size.Y.Offset).toBe(34)
    acc:Expand()
    h.expect(acc.Container.AutomaticSize.Name).toBe("Y")
  end)
  h.it("adding content does not depend on a Heartbeat re-measure (no scheduler write)", function()
    -- Construction + AddX must not throw or require a stepHeartbeat to size correctly; the engine
    -- handles it. (A no-op stepHeartbeat must leave an expanded accordion AutomaticSize.Y.)
    local acc = Accordion.new({ Parent = parentWithLayout(), Title = "X", Expanded = true })
    acc:AddParagraph("a\nb\nc")
    acc:AddButton({ Text = "Go" })
    h.mock.stepHeartbeat(0)
    h.expect(acc.Container.AutomaticSize.Name).toBe("Y")
  end)
end)

h.run()
